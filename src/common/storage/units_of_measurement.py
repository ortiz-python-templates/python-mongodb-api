class UnitsOfMeasurement:
    """Utility class that defines constants for common digital storage units."""

    # Represents one byte (base unit)
    BYTE = 1

    # Represents one kilobyte (1024 bytes)
    KILO_BYTE = BYTE * 1024

    # Represents one megabyte (1024 kilobytes)
    MEGA_BYTE = KILO_BYTE * 1024

    # Represents one gigabyte (1024 megabytes)
    GIGA_BYTE = MEGA_BYTE * 1024

    # Represents one terabyte (1024 gigabytes)
    TERA_BYTE = GIGA_BYTE * 1024
